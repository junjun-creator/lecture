package chapter7ex4.oop1;

public class Stone {
	private int x;
	private int y;
	private int color;
	
	public Stone() {
		this.color = 0;//black
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
	
}
